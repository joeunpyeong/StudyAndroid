package com.example.clonecoding_discord.vo;

public class FriendVO {
	private String friend_id, user_tag, friend_user_tag, status, created_time;

	public String getFriend_id() {
		return friend_id;
	}

	public void setFriend_id(String friend_id) {
		this.friend_id = friend_id;
	}

	public String getUser_tag() {
		return user_tag;
	}

	public void setUser_tag(String user_tag) {
		this.user_tag = user_tag;
	}

	public String getFriend_user_tag() {
		return friend_user_tag;
	}

	public void setFriend_user_tag(String friend_user_tag) {
		this.friend_user_tag = friend_user_tag;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getCreated_time() {
		return created_time;
	}

	public void setCreated_time(String created_time) {
		this.created_time = created_time;
	}
	
}
